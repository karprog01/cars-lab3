public class Car {
    public String brand;
    public String model;
    public int serialNumber;
    public int yearOfIssue;
    public int color;

    public Car(String brand, String model, int serialNumber, int yearOfIssue, int color) {
        this.brand = brand;
        this.model = model;
        this.serialNumber = serialNumber;
        this.yearOfIssue = yearOfIssue;
        this.color = color;
    }
}
